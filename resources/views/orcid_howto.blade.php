<p style='font-size: 20px; font-weight: bold; text-align: center; margin: 20px 0px'>Steps to login to LifeWatchGreece portal using your ORCID account</p>

<p>In order to login using your ORCID account you need to make sure that your ORCID public profile includes 
    a published name and a public email address. If your are not sure about that, follow the steps described below.</p>

<p style='font-weight: bold'>1. Go to ORCID page and select "Sign In"</p>

<p style='text-align: center'><img src="{{ url('images/orcid/orcid1.png') }}"></p>

<p style='font-weight: bold'>2. Register for an ORCID account </p>

<p>Click the "Register for an ORCID id" button. </p>

<p style='text-align: center'><img src="{{ url('images/orcid/orcid2.png') }}"></p>

<p>Complete the registration form and submit it.</p>

<p style='font-weight: bold'>3. Update your public profile</p>

<p>Edit the profile name:</p>
<p style='text-align: center'><img src="{{ url('images/orcid/orcid3.png') }}"></p>

<p>Fill up the "Published Œùame" field and make it public.</p>
<p style='text-align: center'><img src="{{ url('images/orcid/orcid4.png') }}"></p>

<p>Edit the profile's Emails and make your email public</p>
<p style='text-align: center'><img src="{{ url('images/orcid/orcid5.png') }}"></p>
<p style='text-align: center'><img src="{{ url('images/orcid/orcid6.png') }}"></p>

<p>Logout and go to your ORCID public profile (the URL will be http://orcid.org/<your ORCID id> )</p>
<p style='text-align: center'><img src="{{ url('images/orcid/orcid7.png') }}"></p>

<p>If the above steps have been completed, the published name and your email should be displayed on this page!
Now you are ready to login with your ORCID account.</p>

